        print $panel_dtsi_file $dtsi_property_strings[$count][5];
        next;
    }

    if ($dtsi_property_strings[$count][0] eq 'depond-on')
    {
        my $parser_tmp_str = $dtsi_property_strings[$count][1]->($dtsi_property_strings[$count][2]);
